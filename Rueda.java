
public class Rueda {
private String marca;
private String codRueda;

public Rueda(String marca, String codRueda){
	this.marca=marca;
	this.codRueda=codRueda;
	
}
public String getMarca() {
	return marca;
}
public void setMarca(String marca) {
	this.marca = marca;
}
public String getCodRueda() {
	return codRueda;
}
public void setCodRueda(String codRueda) {
	this.codRueda = codRueda;
}

}
